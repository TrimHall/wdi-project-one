  function startGame(event) {
    if (!gameInPlay) {
      gameInPlay = true;
      const audio = document.querySelector('.main');
      audio.setAttribute('src', 'sounds/SayMyName120Bpm.mp3');
      audio.play();
      setTimeout(() => {
        runProgram();
      },4000 );
      event.target.classList.add('hide');
    }
  }

  //                                      **** CHECKS ARRAY INDEX EVERY 250MS ****
  function runProgram() {
    const timing = setInterval(() => {
      fireNotesA();
      fireNotesB();
      fireNotesC();
      fireNotesD();
      updateScore();
      i++;
    }, 250);
    setTimeout(() => {
      clearInterval(timing);
    }, songLength);
  }

  //                                                  **** SCORE KEEPER ****
  let score = 0;

  function updateScore() {
    let oldScore = parseInt(scoreSpan.textContent) || 0;
    const scoreInterval = setInterval(() => {
      if (oldScore < score) {
        if (oldScore >= score) {
          clearInterval(scoreInterval);
        }
        scoreSpan.textContent = oldScore;
        oldScore += 2;
      } else {
        if (oldScore <= score) {
          clearInterval(scoreInterval);
        }
        scoreSpan.textContent = oldScore;
        oldScore -= 2;
      }
    }, 5);
  }
  //                                                 **** FIRES NOTES ****
  let i = 0;

  function fireNotesA() {
    if(odeszaA[i] === 1){
      create(container, notesInPlay);
    }
  }
  function fireNotesB() {
    if(odeszaB[i] === 1){
      create(containerB, notesInPlayB);
    }
  }

  function fireNotesC() {
    if(odeszaC[i] === 1){
      create(containerC, notesInPlayC);
    }
  }

  function fireNotesD() {
    if(odeszaD[i] === 1){
      create(containerD, notesInPlayD);
    }
  }
  //                                       **** CREATE/KILL NOTES BUTTON A ****

  function create(container, notesInPlay) {
    const newNote = document.createElement('div');
    container.appendChild(newNote);
    newNote.classList.add('note');
    notesInPlay.push(newNote);
    newNote.animate([
      {transform: 'translateY(0px)'},
      {transform: 'translateY(680px)'}
    ], 4000);
    setTimeout(() => {
      notesInPlay.splice(0, 1);
      newNote.remove();
    }, 4000);
  }
  //                                       **** KEY PRESSES / COLLISSIONS ****
  $(window).keydown(function(e) {
    if (e.which === 90) {
      checkCol(notesInPlay, targetA);
    } else if (e.which === 88) {
      checkCol(notesInPlayB, targetB);
    } else if (e.which === 67) {
      checkCol(notesInPlayC, targetC);
    } else if (e.which === 86) {
      checkCol(notesInPlayD, targetD);
    }
  });
  //                                         **** COLLISION DETECTION ****
  function checkCol(notesInPlay, target) {
    const position = parseFloat(window.getComputedStyle(notesInPlay[0]).transform.split(',')[5]);
    // console.log(position);
    if(position >= 610 && position <= 670){
      score = score+100;
      currentStreak = currentStreak+1;
      target.classList.add('hit');
      setTimeout(() => {
        target.classList.remove('hit');
      }, 180);
    } else {
      soundEffect.setAttribute('src', 'sounds/bum-note1.mp3');
      soundEffect.play();
      score = score-100;
      currentStreak = 0;
      board.classList.remove('shake');
    }
    updateScore();
    checkCurrentStreak();
  }

